var numberX = 100;
var numberY = 20;


// Example of if statement when the condition is true

if(numberX > numberY){ // 100 > 20 -> True
    console.log("X is greater than Y");
}

// True would mean the decision is true
// and the code inside the if statement will run

var numberA = -1;
var numberB = 0;
if(numberA > numberB) // -1 > 0 -> False
    console.log("A is greater than B");

else
    console.log("B is greater than A");

// False would mean the decision is not true
// and the code inside the if statement will not run